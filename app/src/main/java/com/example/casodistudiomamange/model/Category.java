package com.example.casodistudiomamange.model;
/* Model della categoria di piatto*/
public class Category {
    private String img;
    private String nome;

    public Category() {
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
